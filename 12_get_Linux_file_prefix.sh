#!/bin/bash
Q1_dir="/data1/project/omics_final/Q1"

# 需求：/data1/project/omics_final/Q1/T2T_10.jf 要获取文件前缀输出 T2T_10

# 法1：basename命令

for jf in ${Q1_dir}/*.jf;do 

    prefix=$(basename ${jf} .jf)
    
    echo -e ${prefix} 

done

##############################################################################################################################################################################

# 法2：basename+shell参数扩展

for jf in ${Q1_dir}/*.jf;do 

    jf=$(basename ${jf})

    prefix=${jf%.jf}
    
    echo -e ${prefix} 

done

##############################################################################################################################################################################

# 法3：cut+<<<重定向

for jf in ${Q1_dir}/*.jf;do 

    jf=$(basename ${jf})
    
    prefix=$(cut -d "." -f 1 <<< ${jf})

    echo -e ${prefix} 

done

##############################################################################################################################################################################

# 法4：echo+cut

for jf in ${Q1_dir}/*.jf;do 

    jf=$(basename ${jf})
    
    prefix=$(echo ${jf} | cut -d "." -f 1)

    echo -e ${prefix} 

done

##############################################################################################################################################################################

# 法5：awk+<<<重定向

for jf in ${Q1_dir}/*.jf;do 

    jf=$(basename ${jf})
    
    prefix=$(awk -F "." '{print $1}' <<< ${jf})

    echo -e ${prefix} 

done

##############################################################################################################################################################################

# 法6：echo+awk：

for jf in ${Q1_dir}/*.jf;do 

    jf=$(basename ${jf})
    
    prefix=$(echo ${jf} | awk -F "." '{print $1}')

    echo -e ${prefix} 

done
